// Lesson 1 Variable and Constant
import UIKit

// A variable: is something that can change.
// A constant: is something that does not change.
// a variable as a box that you can open and replace what’s inside.
// a constant is a sealed box — once you put something inside, you can’t change it.

var age = 30
print(age)
age = 40
print(age)

let birthYear = 2000
birthYear = 1000






































